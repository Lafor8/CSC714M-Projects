package io;

public class Article {
	public String title;
	public String author;
	public String date;
	public String body;
	public String link;
	
	public Article(String title, String author, String date, String body, String link){
		this.title = title;
		this.author = author;
		this.date = date;
		this.body = body;
		this.link = link;
	}
}
